<h2 class="h2">Different Types of Services | Bank Accounts</h2>
<h3 class="h3">Individual Banking—Banks typically offer a variety of services to assist individuals in managing their finances, including:</h3>
<ul>
<li>Current accounts</li>
<li>Savings accounts</li>
<li>Debit & credit cards</li>
<li>Insurance</li>
<li>Wealth management</li>
</ul>
<h3 class="h3">Business Banking—Most banks offer financial services for business owners who need to differentiate professional and personal finances. Different types of business banking services include:</h3>
<ul>
<li>Business loans</li>
<li>Merchant services (credit card processing, reconciliation and reporting, check collection)</li>
<li>Cash management (payroll services, deposit services, etc.)</li>
</ul>
<h3 class="h3">Digital Banking—The ability to manage your finances online from your computer, tablet, or smartphone is becoming more and more important to consumers. Banks will typically offer digital banking services that include:</h3>
<ul>
<li>Online, mobile, and tablet banking</li>
<li>Mobile check deposit</li>
<li>Text alerts</li>
<li>eStatements</li>
<li>Online bill pay</li>
</ul>
<h3 class="h3">Loans—Loans are a common banking service offered, and they come in all shapes and sizes.  Some common types of loans that banks provide include:</h3>
<ul>
<li>Personal loans</li>
<li>Home equity loans</li>
<li>Home equity lines of credit</li>
<li>Home loans</li>
<li>Business loans</li>
</ul>
