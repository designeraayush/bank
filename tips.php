<h2 class="h2">Important Security Tips For Safe Online Banking </h2>
<ul>
<li>Access your bank website only by typing the URL in the address bar of your browser.</li>
<li>Be aware of downloading any malicious application from mobile application stores (Google Playstore, Apple App Store, Blackberry App World, Ovi Store, Windows Marketplace etc) that are offering Online Banking. Kindly check their authenticity before downloading, by contacting your Bank.</li>
<li>Do not click on any links in any e-mail message to access the site.</li>
<li>SBI or any of its representative never sends you email/SMS or calls you over phone to get your personal information, password or one time SMS (high security) password. Any such e-mail/SMS or phone call is an attempt to fraudulently withdraw money from your account through Internet Banking. Never respond to such email/SMS or phone call. Please report immediately on report dot phishing at sbi dot co dot in if you receive any such e-mail/SMS or Phone call. Please lock your user access immediately, if you have accidentally revealed your credentials. Click here to lock.</li>
<li>Do not be lured if you receive an e-mail/SMS/phone call promising reward for providing your personal information or for updating your account details in the bank site.</li></ul>
<h3 class="h3">Having the following will improve your internet security:</h3>
<ul>
<li>Newer version of Operating System with latest security patches.</li>
<li>Latest version of Browsers (IE 7.0 and above , Mozilla Firefox 3.1 and above, Opera 9.5 and above, Safari 3.5 and above, Google chrome,etc.)</li>
<li>Firewall is enabled.</li>
<li>Antivirus signatures applied</li>
<li>Scan your computer regularly with Antivirus to ensure that the system is Virus/Trojan free.</li>
<li>Change your Internet Banking password at periodical intervals.</li>
<li>Always check the last log-in date and time in the post login page.</li>
<li>Avoid accessing Internet banking accounts from cyber cafes or shared PCs.</li>
<li>After you have logged in, you will not be asked to provide your username and login password again. Also, you will not be asked to provide your CREDIT or DEBIT CARD details while using internet banking. If you get a message (such as through a pop-up) asking for such information, please do not provide this information no matter how 'genuine' the page appears to be. Such pop-ups are most likely the result of malwares infecting your computer. Please take immediate steps to disinfect your device</li>
</ul>
