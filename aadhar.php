<h2 class="h2">Linking Aadhaar number to the bank account</h2>
<ul>
<li>Log into www.onlinesbi.com</li>
<li>Navigate to "Link your Aadhaar number" under "My Accounts", appearing on the left panel of the screen.</li>
<li>On the next page, select the Account number, input the Aadhaar number and click on Submit.</li>
<li>The last 2 digits of registered mobile number (non-editable) will be displayed to you.</li>
<li>Status of mapping will be advised to you on your registered mobile number.</li>
<li>Note: If you are not yet registered for Internet Banking, you can do so online using your ATM cum Debit Card details. To register click here</li>
</ul>

